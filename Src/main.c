/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  uint32_t adc_ref;		// Variable to store RAW ADC values
  char msg[40];		// Array to print out ADC values through serial UART
  double sum = 0;   // place to sum the values for mean calculation
  int nSamples = 20; // number of samples to take
  uint16_t count;	// ADC Capture Counter

  uint32_t moisture = 0;
  uint32_t voltage_reading = 0;
  uint32_t vdda_voltage = 0;

  double transfer_function_result = 0;

  // Polynomail Regression coeffcicents
  double var_A = 1.0509188409e-11;
  double var_B = -1.1372598225e-07;
  double var_C = 4.5893681046e-04;
  double var_D = -8.2813240419e-01;
  double var_E = 5.8035573202e02;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  count = 0;
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

    // Capture samples of ADC Raw data and transmit through Serial Communications using UART protocol
    if (count <= nSamples)
    {
      //Set LD3 LED to High - indicating start of capture
      HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_SET);

      // Getting ADC value
      HAL_ADC_Start(&hadc1); 							// Start the ADC capture
      HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY); 	// Begin Reading ADC_CAL
      adc_ref = HAL_ADC_GetValue(&hadc1);					// Store ADC raw data
      HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY); 	// Begin Reading A0 value
      moisture = HAL_ADC_GetValue(&hadc1);


      // calcualating vdda voltage based on in mV using a uint32_t
      // using adc_raw and the internal calibration value

      vdda_voltage = __HAL_ADC_CALC_VREFANALOG_VOLTAGE(adc_ref, ADC_RESOLUTION_12B);

      // Convert to string and print
      sprintf(msg, "ADC Reference Value = %hu\r\n", adc_ref);			// Create a message to send over UART

      HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY); // Send message over UART

      sprintf(msg, "ADC Moisture Value = %hu\r\n", moisture);			// Create a message to send over UART

      HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY); // Send message over UART

      // Calculate voltage reading from A0

      voltage_reading = __HAL_ADC_CALC_DATA_TO_VOLTAGE(vdda_voltage, moisture, ADC_RESOLUTION_12B);

      sprintf(msg, "Voltage Reading = %hu\r\n", voltage_reading);			// Create a message to send over UART

      HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY); // Send message over UART

      // Pretend we have to do something else for a while
      HAL_Delay(1);			// Capture every second
      count++;					// Increment loop counter

      sum += voltage_reading;
    }
    else if (count == nSamples + 1)
    {
      count++;
      sum = sum / (nSamples + 1);
      sprintf(msg, "Mean Voltage Value = %4.2f\r\n", sum);			// Create a message to send over UART
      HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY); // Send message over UART

      // Calculating the transfer function

      double x = sum;

      transfer_function_result =
      var_A*x*x*x*x
    + var_B*x*x*x
    + var_C*x*x
    + var_D*x
    + var_E;

      sprintf(msg, "Transfer Function (mL) = %4.2f\r\n", transfer_function_result);			// Create a message to send over UART
      HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY); // Send message over UART
    }
    else
    {
      HAL_ADC_Stop(&hadc1);					// Stop the ADC Capture after 20 cycles

    }

    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_RESET); 		// Set LD3 LED to indicate ADC has stopped acquisition

  }

}
